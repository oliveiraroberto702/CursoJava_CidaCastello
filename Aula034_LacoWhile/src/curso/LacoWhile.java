package curso;

public class LacoWhile {
	
	public static void main(String[] args) {
	// laço while - loop
		// inicalização
		// incremento
		// critério de parada
		
		// sintaxe
		// inicialização
		int i = 0;
		// critério de parada
		while (i <= 5) {
			System.out.println(i);
			// incremento
			i +=1; // i++ == i = i + 1
			
		}
		System.out.println("Depois do laço");
	}
}
			
		
		