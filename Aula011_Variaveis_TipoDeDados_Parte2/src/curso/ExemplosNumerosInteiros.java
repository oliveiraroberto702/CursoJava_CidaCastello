package curso;
/**
 * @author Roberto Oliveira
 * Programa para exemplificar tipos de dados e variáveis
 * do tipo inteiro
 */

public class ExemplosNumerosInteiros {
	
	public static void main (String[] args) {
		
		// variáveis do tipo inteiro
		// byte
		// -128 ate 127
		byte pontuacao = -125;
		byte mesesDoAno = 12;
		
		// short
		// -32768 até 32767
		short inteiroShort = 32767;
		
		// int
		// -214748364 até 214748367
		int idade = 53;
		int a =25, b = -365, c;
		c = 5;
			
		
		// long
		// -9223372036854775808 até 9223372036854775807
		long inteiroLongo = 9223372036854775807L;
		System.out.println(inteiroLongo);
		
		
		
		
		
		
	}

}
